// los operedores de asignacion son los que se encargan de asignar un valor a una variable, el mas comun es el operador de asignacion simple (=) pero existen otros operadores de asignacion compuestos que combinan la asignacion con una operacion aritmetica, logica o de bits.


let a = 10; // operador de asignacion simple
a = a + 5; // operador de asignacion compuesto (suma)

a += 5; //
a -= 5; // operador de asignacion compuesto (resta)
a *= 5; // operador de asignacion compuesto (multiplicacion)
a /= 5; // operador de asignacion compuesto (division)
a %= 5; // operador de asignacion compuesto (modulo)
a **= 5; // operador de asignacion compuesto (potencia)

console.log(a); // 20
