// los arrays son un tipo de dato que se utiliza para almacenar una colección de elementos. Son similares a los objetos, pero están diseñados específicamente para almacenar listas ordenadas de valores. Los arrays pueden contener cualquier tipo de dato, incluyendo números, cadenas, objetos e incluso otros arrays. Se accede a los elementos de un array utilizando índices numéricos, comenzando desde 0. Los arrays también tienen métodos incorporados que facilitan la manipulación de sus elementos, como agregar, eliminar o buscar elementos dentro del array.

let animales = ["perro", "gato", "conejo", "hamster"];
console.log(animales); 
console.log(animales[0]); // accediendo al primer elemento del array
console.log(animales[2]); // accediendo al tercer elemento del array

animales[1]="loro"; // modificando el segundo elemento del array
console.log(animales);