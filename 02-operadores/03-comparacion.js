let a = 10;

// OPERADORES DE COMPARACION

console.lot(a > 5); // mayor que
console.log(a < 5); // menor que
console.log(a >= 5); // mayor o igual que
console.log(a <= 5); // menor o igual que

console.log(a == 5); // igual que
console.log(a != 5); // diferente que
console.log(a === 5); // estrictamente igual que
console.log(a !== 5); // estrictamente diferente que